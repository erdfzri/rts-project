// Language switcher logic
document.addEventListener('DOMContentLoaded', function () {
  const languageOptions = document.querySelectorAll('.language-options li');
  const selectedLang = document.querySelector('.selected-lang');
  const selectedLangText = selectedLang.querySelector('span');
  const selectedLangIcon = selectedLang.querySelector('.flag-icon');
  const languageOptionsContainer = document.querySelector('.language-options');

  // Get language from URL or localStorage, default to 'id'
  const urlParams = new URLSearchParams(window.location.search);
  let currentLang = urlParams.get('lang') || localStorage.getItem('language') || 'id';
  
  // Validate language to ensure it's either 'id' or 'en'
  if (!['id', 'en'].includes(currentLang)) {
    currentLang = 'id';
  }
  
  // Save initial language to localStorage
  localStorage.setItem('language', currentLang);
  
  // Update content with initial language
  updateLanguage(currentLang);

  // Toggle language options dropdown
  selectedLang.addEventListener('click', () => {
    languageOptionsContainer.classList.toggle('active');
  });

  // Handle language selection
  languageOptions.forEach(option => {
    option.addEventListener('click', (event) => {
      event.preventDefault(); // Prevent default link behavior
      const lang = option.getAttribute('data-lang');
      currentLang = lang;
      localStorage.setItem('language', lang);

      // Get current service ID from URL
      const serviceId = urlParams.get('id');

      // Update URL with new language while preserving service ID
      const newUrl = new URL(window.location);
      newUrl.searchParams.set('lang', lang);
      if (serviceId) {
        newUrl.searchParams.set('id', serviceId);
      } else {
        newUrl.searchParams.delete('id'); // Remove id if not present
      }
      window.history.pushState({}, '', newUrl);

      // Update content immediately
      updateLanguage(lang);
      languageOptionsContainer.classList.remove('active');
    });
  });

  // Close dropdown when clicking outside
  document.addEventListener('click', (event) => {
    if (!selectedLang.contains(event.target) && !languageOptionsContainer.contains(event.target)) {
      languageOptionsContainer.classList.remove('active');
    }
  });

  // Listen for popstate to handle browser back/forward buttons
  window.addEventListener('popstate', () => {
    const newParams = new URLSearchParams(window.location.search);
    const newLang = newParams.get('lang') || localStorage.getItem('language') || 'id';
    if (['id', 'en'].includes(newLang)) {
      updateLanguage(newLang);
    }
  });

  // Function to update language content
  function updateLanguage(lang) {
    // Update selected language display
    selectedLangText.textContent = lang === 'id' ? 'Indonesia' : 'English';
    selectedLangIcon.src = lang === 'id' ? 'img/indonesia.png' : 'img/inggris.png';

    // Update navigation links dynamically based on current page
    const currentPage = window.location.pathname.split('/').pop();
    const isServicesPage = currentPage.includes('services.html');
    const urlParams = new URLSearchParams(window.location.search);
    const serviceId = urlParams.get('id');

    // Update navigation links
    const navHome = document.querySelector('a[href="index.html"]');
    if (navHome) {
      navHome.textContent = translations[lang]['nav-home'];
      navHome.href = 'index.html' + (lang !== 'id' ? '?lang=' + lang : '');
    }

    const navServices = document.querySelector('a[href="index.html#services"]');
    if (navServices) {
      navServices.textContent = translations[lang]['nav-services'];
      navServices.href = isServicesPage ? 'services.html' + (serviceId ? `?id=${serviceId}` : '') + (lang !== 'id' ? `&lang=${lang}` : '') : 'index.html#services' + (lang !== 'id' ? '?lang=' + lang : '');
    }

    const navAbout = document.querySelector('a[href="index.html#about"]');
    if (navAbout) {
      navAbout.textContent = translations[lang]['nav-about'];
      navAbout.href = 'index.html#about' + (lang !== 'id' ? '?lang=' + lang : '');
    }

    const navClients = document.querySelector('a[href="index.html#clients"]');
    if (navClients) {
      navClients.textContent = translations[lang]['nav-clients'];
      navClients.href = 'index.html#clients' + (lang !== 'id' ? '?lang=' + lang : '');
    }

    const navContact = document.querySelector('a[href="index.html#contact"]');
    if (navContact) {
      navContact.textContent = translations[lang]['nav-contact'];
      navContact.href = 'index.html#contact' + (lang !== 'id' ? '?lang=' + lang : '');
    }

    // Update services hero section
    const heroTitle = document.querySelector('.services-hero .section-header h2');
    if (heroTitle) {
      heroTitle.innerHTML = translations[lang]['services-hero-title'].replace('Kami', `<span>Kami</span>`);
    }
    const heroSubtitle = document.querySelector('.services-hero .section-header p');
    if (heroSubtitle) {
      heroSubtitle.textContent = translations[lang]['services-hero-subtitle'];
    }

    // Update highlight cards
    const highlightCards = document.querySelectorAll('.highlight-card');
    if (highlightCards.length >= 3) {
      highlightCards[0].querySelector('h4').textContent = translations[lang]['highlight-fast-delivery'];
      highlightCards[0].querySelector('p').textContent = translations[lang]['highlight-fast-delivery-desc'];
      highlightCards[1].querySelector('h4').textContent = translations[lang]['highlight-unlimited-revisions'];
      highlightCards[1].querySelector('p').textContent = translations[lang]['highlight-unlimited-revisions-desc'];
      highlightCards[2].querySelector('h4').textContent = translations[lang]['highlight-support'];
      highlightCards[2].querySelector('p').textContent = translations[lang]['highlight-support-desc'];
    }

    // Update section titles
    const featuresTitle = document.querySelector('.service-features h3');
    if (featuresTitle) {
      featuresTitle.textContent = translations[lang]['features-title'];
    }
    const processTitle = document.querySelector('.service-process h3');
    if (processTitle) {
      processTitle.textContent = translations[lang]['process-title'];
    }
    const examplesTitle = document.querySelector('.service-examples h3');
    if (examplesTitle) {
      examplesTitle.textContent = translations[lang]['examples-title'];
    }
    const faqTitle = document.querySelector('.service-faq h3');
    if (faqTitle) {
      faqTitle.textContent = translations[lang]['faq-title'];
    }
    const ctaTitle = document.querySelector('.service-cta h3');
    if (ctaTitle) {
      ctaTitle.textContent = translations[lang]['cta-title'];
    }
    const ctaDescription = document.querySelector('.service-cta p');
    if (ctaDescription) {
      ctaDescription.textContent = translations[lang]['cta-description'];
    }
    const ctaButton = document.querySelector('.service-cta .btn-primary');
    if (ctaButton) {
      ctaButton.textContent = translations[lang]['cta-button'];
    }

    // Update footer
    const footerAbout = document.querySelector('.footer-about p');
    if (footerAbout) {
      footerAbout.textContent = translations[lang]['footer-about'];
    }
    const footerLinksTitle = document.querySelector('.footer-links h4');
    if (footerLinksTitle) {
      footerLinksTitle.textContent = translations[lang]['footer-quick-links'];
    }
    const footerServicesTitle = document.querySelector('.footer-services h4');
    if (footerServicesTitle) {
      footerServicesTitle.textContent = translations[lang]['footer-services'];
    }
    const footerNewsletterTitle = document.querySelector('.footer-newsletter h4');
    if (footerNewsletterTitle) {
      footerNewsletterTitle.textContent = translations[lang]['footer-newsletter'];
    }
    const footerNewsletterDesc = document.querySelector('.footer-newsletter p');
    if (footerNewsletterDesc) {
      footerNewsletterDesc.textContent = translations[lang]['footer-newsletter-desc'];
    }
    const footerNewsletterInput = document.querySelector('.footer-newsletter input');
    if (footerNewsletterInput) {
      footerNewsletterInput.placeholder = translations[lang]['footer-newsletter-placeholder'];
    }
    const footerCopyright = document.querySelector('.footer-bottom p');
    if (footerCopyright) {
      footerCopyright.textContent = translations[lang]['footer-copyright'];
    }
    const footerPrivacy = document.querySelector('.legal-links a[href="#"]:first-child');
    if (footerPrivacy) {
      footerPrivacy.textContent = translations[lang]['footer-privacy'];
    }
    const footerTerms = document.querySelector('.legal-links a[href="#"]:last-child');
    if (footerTerms) {
      footerTerms.textContent = translations[lang]['footer-terms'];
    }

    // Update footer services links
    const footerServices = document.querySelectorAll('.footer-services a');
    if (footerServices.length >= 4) {
      footerServices[0].textContent = translations[lang].services['video-editor'].title;
      footerServices[0].href = `services.html?id=video-editor${lang !== 'id' ? '&lang=' + lang : ''}`;
      footerServices[1].textContent = translations[lang].services['visual-artist'].title;
      footerServices[1].href = `services.html?id=visual-artist${lang !== 'id' ? '&lang=' + lang : ''}`;
      footerServices[2].textContent = translations[lang].services['visual-jockey'].title;
      footerServices[2].href = `services.html?id=visual-jockey${lang !== 'id' ? '&lang=' + lang : ''}`;
      footerServices[3].textContent = translations[lang].services['ai-content'].title;
      footerServices[3].href = `services.html?id=ai-content${lang !== 'id' ? '&lang=' + lang : ''}`;
    }

    // Update service details based on URL parameter
    if (serviceId && translations[lang].services[serviceId]) {
      const service = translations[lang].services[serviceId];
      const originalService = services[serviceId];

      // Update page title
      document.title = `${service.title} | Vision Studio`;

      // Update service header
      const serviceTitle = document.getElementById('service-title');
      if (serviceTitle) {
        serviceTitle.textContent = service.title;
      }
      const serviceDescription = document.getElementById('service-description');
      if (serviceDescription) {
        serviceDescription.textContent = service.description;
      }
      const deliveryTime = document.getElementById('delivery-time');
      if (deliveryTime) {
        deliveryTime.textContent = service.deliveryTime;
      }
      const startingPrice = document.getElementById('starting-price');
      if (startingPrice) {
        startingPrice.textContent = service.startingPrice;
      }
      const popularity = document.getElementById('popularity');
      if (popularity) {
        popularity.textContent = service.popularity;
      }
      const serviceIcon = document.getElementById('service-icon');
      if (serviceIcon) {
        serviceIcon.innerHTML = originalService.icon;
      }

      // Update features
      const featuresList = document.getElementById('service-features-list');
      if (featuresList) {
        featuresList.innerHTML = '';
        service.features.forEach(feature => {
          const li = document.createElement('li');
          li.innerHTML = `<i class="fas fa-check"></i> ${feature}`;
          featuresList.appendChild(li);
        });
      }

      // Update process
      const processSteps = document.getElementById('process-steps');
      if (processSteps) {
        processSteps.innerHTML = '';
        service.process.forEach((step, index) => {
          const stepDiv = document.createElement('div');
          stepDiv.className = 'process-step';
          stepDiv.innerHTML = `
            <div class="step-number">${index + 1}</div>
            <div class="step-content">
              <h4>${step.title}</h4>
              <p>${step.description}</p>
            </div>
          `;
          processSteps.appendChild(stepDiv);
        });
      }

      // Update FAQs
      const faqAccordion = document.getElementById('faq-accordion');
      if (faqAccordion && service.faqs) {
        faqAccordion.innerHTML = '';
        service.faqs.forEach(faq => {
          const faqItem = document.createElement('div');
          faqItem.className = 'faq-item';
          faqItem.innerHTML = `
            <div class="faq-question">
              <h4>${faq.question}</h4>
              <i class="fas fa-chevron-down"></i>
            </div>
            <div class="faq-answer">
              <div class="faq-answer-inner">
                <p>${faq.answer}</p>
              </div>
            </div>
          `;
          faqAccordion.appendChild(faqItem);

          // Accordion toggle
          const question = faqItem.querySelector('.faq-question');
          question.addEventListener('click', () => {
            faqItem.classList.toggle('active');
          });
        });
      }

      // Update examples
      const examplesGrid = document.getElementById('examples-grid');
      if (examplesGrid && service.examples) {
        examplesGrid.innerHTML = '';
        const originalService = services[serviceId];
        service.examples.forEach((example, index) => {
          const exampleDiv = document.createElement('div');
          exampleDiv.className = 'example-item';
          const originalExample = originalService.examples[index] || {};

          if (originalExample.videoId) {
            exampleDiv.innerHTML = `
              <div class="video-container" data-video-id="${originalExample.videoId}">
                <img src="${originalExample.thumbnail}" alt="${example.title}" class="video-thumbnail">
                <div class="play-button"><i class="fas fa-play"></i></div>
                <div class="example-overlay">
                  <h4>${example.title}</h4>
                </div>
              </div>
            `;
            exampleDiv.querySelector('.video-container').addEventListener('click', function () {
              const videoId = this.getAttribute('data-video-id');
              this.innerHTML = `
                <iframe width="100%" height="100%" 
                  src="https://www.youtube.com/embed/${videoId}?autoplay=1&rel=0" 
                  frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen>
                </iframe>
              `;
            });
          } else {
            exampleDiv.innerHTML = `
              <a href="${originalExample.link}">
                <img src="${originalExample.image}" alt="${example.title}">
                <div class="example-overlay">
                  <h4>${example.title}</h4>
                </div>
              </a>
            `;
          }
          examplesGrid.appendChild(exampleDiv);
        });
      }

      // Animate populated elements
      const animateElements = () => {
        const featureItems = document.querySelectorAll('.service-features li');
        featureItems.forEach((item, index) => {
          item.style.animationDelay = `${index * 0.1}s`;
        });

        const processItems = document.querySelectorAll('.process-step');
        processItems.forEach((item, index) => {
          item.style.animationDelay = `${index * 0.2 + 0.1}s`;
        });

        const exampleItems = document.querySelectorAll('.example-item');
        exampleItems.forEach((item, index) => {
          item.style.animationDelay = `${index * 0.2 + 0.1}s`;
        });

        const faqItems = document.querySelectorAll('.faq-item');
        faqItems.forEach((item, index) => {
          item.style.animationDelay = `${index * 0.2 + 0.1}s`;
        });

        setTimeout(() => {
          if (typeof AOS !== 'undefined') {
            AOS.refresh();
          }
        }, 500);
      };

      setTimeout(animateElements, 300);
    }
  }
});